class FeatureFusionModule(nn.Module):
    def __init__(self, in_chan, out_chan, *args, **kwargs):
        super(FeatureFusionModule, self).__init__()
        self.convblk = ConvBNReLU(in_chan, out_chan, ks=1, stride=1, padding=0)
        self.conv1 = nn.Conv2d(out_chan,
                out_chan//4,
                kernel_size = 1,
                stride = 1,
                padding = 0,
                bias = False)
        self.conv2 = nn.Conv2d(out_chan//4,
                out_chan,
                kernel_size = 1,
                stride = 1,
                padding = 0,
                bias = False)
        self.relu = nn.ReLU(inplace=True)
        self.sigmoid = nn.Sigmoid()
        self.init_weight()

    def forward(self, fsp, fcp):
        fcat = torch.cat([fsp, fcp], dim=1)
        feat = self.convblk(fcat)
        atten = F.avg_pool2d(feat, feat.size()[2:])
        atten = self.conv1(atten)
        atten = self.relu(atten)
        atten = self.conv2(atten)
        atten = self.sigmoid(atten)
        feat_atten = torch.mul(feat, atten)
        feat_out = feat_atten + feat
        return feat_out

    def init_weight(self):
        for ly in self.children():
            if isinstance(ly, nn.Conv2d):
                nn.init.kaiming_normal_(ly.weight, a=1)
                if not ly.bias is None: nn.init.constant_(ly.bias, 0)

    def get_params(self):
        wd_params, nowd_params = [], []
        for name, module in self.named_modules():
            if isinstance(module, (nn.Linear, nn.Conv2d)):
                wd_params.append(module.weight)
                if not module.bias is None:
                    nowd_params.append(module.bias)
            elif isinstance(module, BatchNorm2d):
                nowd_params += list(module.parameters())
        return wd_params, nowd_params

class BiSeNet(nn.Module):
    def __init__(self, n_classes, *args, **kwargs):
        super(BiSeNet, self).__init__()
        self.cp = ContextPath()
        self.sp = SpatialPath()
        self.ffm = FeatureFusionModule(256, 256)
        self.conv_out = BiSeNetOutput(256, 256, n_classes)
        self.conv_out16 = BiSeNetOutput(128, 64, n_classes)
        self.conv_out32 = BiSeNetOutput(128, 64, n_classes)
        self.init_weight()

    def forward(self, x):
        H, W = x.size()[2:]
        feat_cp8, feat_cp16 = self.cp(x)
        feat_sp = self.sp(x)
        feat_fuse = self.ffm(feat_sp, feat_cp8)

        feat_out = self.conv_out(feat_fuse)
        feat_out16 = self.conv_out16(feat_cp8)
        feat_out32 = self.conv_out32(feat_cp16)

        feat_out = F.interpolate(feat_out, (H, W), mode='bilinear', align_corners=True)
        feat_out16 = F.interpolate(feat_out16, (H, W), mode='bilinear', align_corners=True)
        feat_out32 = F.interpolate(feat_out32, (H, W), mode='bilinear', align_corners=True)
        return feat_out, feat_out16, feat_out32

    def init_weight(self):
        for ly in self.children():
            if isinstance(ly, nn.Conv2d):
                nn.init.kaiming_normal_(ly.weight, a=1)
                if not ly.bias is None: nn.init.constant_(ly.bias, 0)

    def get_params(self):
        wd_params, nowd_params, lr_mul_wd_params, lr_mul_nowd_params = [], [], [], []
        for name, child in self.named_children():
            child_wd_params, child_nowd_params = child.get_params()
            if isinstance(child, (FeatureFusionModule, BiSeNetOutput)):
                lr_mul_wd_params += child_wd_params
                lr_mul_nowd_params += child_nowd_params
            else:
                wd_params += child_wd_params
                nowd_params += child_nowd_params
        return wd_params, nowd_params, lr_mul_wd_params, lr_mul_nowd_params

def conv3x3(in_planes, out_planes, stride=1, groups=1, dilation=1):
    """3x3 convolution with padding"""
    return nn.Conv2d(in_planes, out_planes, kernel_size=3, stride=stride,
                     padding=dilation, groups=groups, bias=False, dilation=dilation)

def conv1x1(in_planes, out_planes, stride=1):
    """1x1 convolution"""
    return nn.Conv2d(in_planes, out_planes, kernel_size=1, stride=stride, bias=False)



class BasicBlock(nn.Module):
    expansion = 1

    def __init__(self, inplanes, planes, stride=1, downsample=None):
        super(BasicBlock, self).__init__()
        self.conv1 = conv3x3(inplanes, planes, stride)
        self.bn1 = nn.BatchNorm2d(planes)
        self.conv2 = conv3x3(planes, planes)
        self.bn2 = nn.BatchNorm2d(planes)
        self.relu = nn.ReLU(inplace=True)
        self.downsample = downsample
        self.stride = stride

    def forward(self, x):
        residual = x
        out = self.conv1(x)
        out = self.bn1(out)
        out = self.relu(out)
        out = self.conv2(out)
        out = self.bn2(out)

        if self.downsample is not None:
            residual = self.downsample(x)
        out += residual
        out = self.relu(out)
        return out



class CAGFace(nn.Module):

    def __init__(self,stage=2):
        super(CAGFace, self).__init__()
        self.stage=stage
        self.stemLayer = nn.Sequential(conv3x3(6,256),
                                       nn.BatchNorm2d(256),
                                       nn.ReLU(inplace=True))
        resblock1 = [BasicBlock(256,256) for i in range(16)]
        self.Backbone1 = nn.Sequential(*resblock1)
        self.SpatialUpSampling1 = nn.Sequential(conv3x3(256,256),
                                                nn.BatchNorm2d(256),
                                                nn.ReLU(inplace=True),
                                                conv3x3(256,1024),
                                                nn.BatchNorm2d(1024),
                                                nn.ReLU(inplace=True),
                                                DepthToSpace(2),
                                                conv3x3(256,3),
                                                nn.BatchNorm2d(3),
                                                nn.ReLU(inplace=True),
                                                conv3x3(3,3),
                                                nn.BatchNorm2d(3),
                                                nn.Sigmoid())

        if(stage==2):
            self.downsample = nn.Sequential(SpaceToDepth(2),
                                            conv3x3(12,256))
            self.stemLayer2 = nn.Sequential(conv3x3(256,256),
                                            nn.ReLU(inplace=True))
            resblock2 = [BasicBlock(256,256) for i in range(16)]
            self.Backbone2 = nn.Sequential(*resblock2)
            self.SpatialUpSampling2 = nn.Sequential(conv3x3(256,256),
                                                nn.ReLU(inplace=True),
                                                conv1x1(256,512),
                                                nn.ReLU(inplace=True),
                                                conv3x3(512,2048),
                                                nn.ReLU(inplace=True),
                                                DepthToSpace(2),
                                                conv3x3(512,2048),
                                                nn.ReLU(inplace=True),
                                                DepthToSpace(2),
                                                conv3x3(512,3),
                                                nn.ReLU(inplace=True),
                                                conv3x3(3,3),
                                                nn.ReLU(inplace=True))


    def forward(self, x):
        x = self.stemLayer(x)
        residual = self.Backbone1(x)

        out1 = x + residual
        out1 = nn.ReLU(inplace=True)(out1)
        out1 = self.SpatialUpSampling1(out1)

        if(self.stage==2):
            stage2 = self.downsample(out1)
            stage2 = self.stemLayer2(stage2)
            stage2 = self.Backbone1(stage2)

            out2 = stage2+x
            out2 = self.SpatialUpSampling2(out2)
            return out1,out2

        else:
            return out1


